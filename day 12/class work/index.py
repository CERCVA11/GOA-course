# რიცხვის მიღება მომხმარებლისგან, როგორც string
num_str = input("Enter a number: ")
print(f"Data type of num_str: {type(num_str)}")  # string ტიპი

# string-ის გარდაქმნა integer-ად
num_int = int(num_str)
print(f"Data type of num_int: {type(num_int)}")  # integer ტიპი

# integer-ის გარდაქმნა float-ად
num_float = float(num_int)
print(f"Data type of num_float: {type(num_float)}")  # float ტიპი